    """Load either the component or system metrics report."""
    metrics_dir = Path("metrics")
    results_dir = Path("results")
    report_map = {
        "component": metrics_dir / "component_metrics_report.csv",
        "system": results_dir / "system_metrics_report.csv",
    }
    df = pd.read_csv(report_map[request.param])
    df["report_type"] = request.param
    return df

