@pytest.mark.parametrize("id_type,getter", [
    ("ex_sample", md.get_ex_sample_ids),
    ("ms_sample", md.get_ms_sample_ids),
    ("ex_lane",   md.get_ex_lane_ids),
])
def test_component_uniformity(lightweight_test_run, config, id_type, getter):
    """Ensure all component-level samples/lanes have uniform metric counts."""
    metrics_path = Path("metrics/component_metrics_report.csv")
    df = pd.read_csv(metrics_path)

    sample_ids = getter(config)
    sub_df = df[df["Sample"].isin(sample_ids)]
    counts = sub_df["Sample"].value_counts()

    assert len(set(counts)) == 1, (
        f"[component] {id_type}s have unequal counts: {counts.to_dict()}"
    )